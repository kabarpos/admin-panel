// Helper function untuk mengubah format nama kota/kabupaten
const formatCityName = (name) => {
  return name.split(' ').map((word, index) => {
    // Jika kata pertama (KABUPATEN/KOTA), gunakan Title Case
    if (index === 0) {
      return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
    }
    // Untuk kata-kata setelahnya, gunakan Title Case
    return word.charAt(0).toUpperCase() + word.slice(1).toLowerCase();
  }).join(' ');
};

